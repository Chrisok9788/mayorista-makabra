create table if not exists public.pedido_items (
  id bigint generated by default as identity primary key,
  pedido_order_id text,
  producto_id text,
  producto_nombre text,
  cantidad integer default 1,
  precio_unitario_uyu integer default 0,
  subtotal_uyu integer default 0,
  consultable boolean default false,
  armado boolean default false,
  sector text,
  creado_en timestamptz default now(),
  actualizado_en timestamptz default now()
);

alter table public.pedido_items
  add column if not exists pedido_order_id text,
  add column if not exists producto_id text,
  add column if not exists producto_nombre text,
  add column if not exists cantidad integer default 1,
  add column if not exists precio_unitario_uyu integer default 0,
  add column if not exists subtotal_uyu integer default 0,
  add column if not exists consultable boolean default false,
  add column if not exists armado boolean default false,
  add column if not exists sector text,
  add column if not exists creado_en timestamptz default now(),
  add column if not exists actualizado_en timestamptz default now();

-- Descarta solamente filas inválidas de la tabla operativa nueva.
delete from public.pedido_items
 where pedido_order_id is null
    or producto_id is null;

-- Conserva una sola fila por producto si hubo una ejecución parcial previa.
delete from public.pedido_items as older
using public.pedido_items as newer
where older.ctid < newer.ctid
  and older.pedido_order_id = newer.pedido_order_id
  and older.producto_id = newer.producto_id;

create unique index if not exists pedido_items_order_product_unique_idx
  on public.pedido_items (pedido_order_id, producto_id);
create index if not exists pedido_items_pedido_idx
  on public.pedido_items (pedido_order_id);

-- Recupera productos guardados anteriormente dentro de pedidos.items o
-- pedidos.articulos. Si esas columnas no existen, to_jsonb simplemente devuelve null.
with legacy_rows as (
  select
    p.order_id,
    coalesce(to_jsonb(p) -> 'items', to_jsonb(p) -> 'articulos', '[]'::jsonb) as payload
  from public.pedidos as p
), expanded as (
  select
    l.order_id,
    element.item,
    element.position
  from legacy_rows as l
  cross join lateral jsonb_array_elements(
    case
      when jsonb_typeof(l.payload) = 'array' then l.payload
      else '[]'::jsonb
    end
  ) with ordinality as element(item, position)
), normalized as (
  select
    order_id,
    coalesce(
      nullif(item ->> 'productId', ''),
      nullif(item ->> 'producto_id', ''),
      nullif(item ->> 'id', ''),
      'legacy-' || md5(
        order_id || coalesce(item ->> 'name', item ->> 'nombre', 'Producto') || position::text
      )
    ) as producto_id,
    coalesce(
      nullif(item ->> 'name', ''),
      nullif(item ->> 'nombre', ''),
      'Producto sin nombre'
    ) as producto_nombre,
    case
      when replace(coalesce(item ->> 'qty', item ->> 'cantidad', ''), ',', '.')
           ~ '^[0-9]+([.][0-9]+)?$'
      then greatest(
        1,
        round(replace(coalesce(item ->> 'qty', item ->> 'cantidad'), ',', '.')::numeric)::integer
      )
      else 1
    end as cantidad,
    case
      when replace(
        coalesce(
          item ->> 'unitPriceRounded',
          item ->> 'precio_unitario_uyu',
          item ->> 'precio_unitario',
          item ->> 'precio',
          ''
        ),
        ',', '.'
      ) ~ '^[0-9]+([.][0-9]+)?$'
      then round(replace(
        coalesce(
          item ->> 'unitPriceRounded',
          item ->> 'precio_unitario_uyu',
          item ->> 'precio_unitario',
          item ->> 'precio'
        ),
        ',', '.'
      )::numeric)::integer
      else 0
    end as precio_unitario_uyu,
    case
      when replace(
        coalesce(item ->> 'subtotalRounded', item ->> 'subtotal_uyu', item ->> 'subtotal', ''),
        ',', '.'
      ) ~ '^[0-9]+([.][0-9]+)?$'
      then round(replace(
        coalesce(item ->> 'subtotalRounded', item ->> 'subtotal_uyu', item ->> 'subtotal'),
        ',', '.'
      )::numeric)::integer
      else 0
    end as subtotal_uyu,
    lower(coalesce(item ->> 'consultable', 'false')) in ('true', 't', '1', 'si', 'sí')
      as consultable,
    coalesce(
      nullif(item ->> 'sector', ''),
      nullif(item ->> 'category', ''),
      nullif(item ->> 'categoria', '')
    ) as sector
  from expanded
)
insert into public.pedido_items (
  pedido_order_id,
  producto_id,
  producto_nombre,
  cantidad,
  precio_unitario_uyu,
  subtotal_uyu,
  consultable,
  armado,
  sector,
  actualizado_en
)
select
  order_id,
  producto_id,
  producto_nombre,
  cantidad,
  precio_unitario_uyu,
  case
    when subtotal_uyu > 0 then subtotal_uyu
    else precio_unitario_uyu * cantidad
  end,
  consultable or precio_unitario_uyu <= 0,
  false,
  sector,
  now()
from normalized
on conflict (pedido_order_id, producto_id)
do update set
  producto_nombre = excluded.producto_nombre,
  cantidad = excluded.cantidad,
  precio_unitario_uyu = excluded.precio_unitario_uyu,
  subtotal_uyu = excluded.subtotal_uyu,
  consultable = excluded.consultable,
  sector = coalesce(public.pedido_items.sector, excluded.sector),
  actualizado_en = now();

-- ---------------------------------------------------------------------------
-- 3. Control de reintentos y segundos pedidos
-- ---------------------------------------------------------------------------

create table if not exists public.pedido_ingresos (
  order_id text,
  pedido_order_id text,
  creado_en timestamptz default now()
);

alter table public.pedido_ingresos
  add column if not exists order_id text,
  add column if not exists pedido_order_id text,
  add column if not exists creado_en timestamptz default now();

delete from public.pedido_ingresos
 where order_id is null
    or pedido_order_id is null;

delete from public.pedido_ingresos as older
using public.pedido_ingresos as newer
where older.ctid < newer.ctid
  and older.order_id = newer.order_id;

create unique index if not exists pedido_ingresos_order_unique_idx
  on public.pedido_ingresos (order_id);
create index if not exists pedido_ingresos_pedido_idx
  on public.pedido_ingresos (pedido_order_id);

-- Marca como procesados los pedidos que ya estaban almacenados.
insert into public.pedido_ingresos (order_id, pedido_order_id, creado_en)
select order_id, order_id, coalesce(creado_en, now())
from public.pedidos
where order_id is not null
on conflict (order_id) do nothing;

